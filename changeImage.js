        function imagefun() {

            var Image_Id = document.getElementById('getImage');

            if (Image_Id.src.match("/assets/glasses/glass-empty.png")) {
                Image_Id.src = "/assets/glasses/glass-full.png";
            } else {
                Image_Id.src = "/assets/glasses/glass-empty.png";
            }
        }  
        
